function pushbutton5_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton5 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global I
I=imread('Signature.jpg');
I2=imresize(I,[512 ,512]);
bw=rgb2gray(I2);
I3= medfilt2(bw);
%I3=im2bw(I3);
%Counting the black pixels of original image
Co=0;
for i=1:512
    for j=1:512
        if(I3(i,j)==0)
            Co=Co+1;
        end
    end
end

%Counting the black pixels of input image
global Img
Img=imread('Signature.jpg');
Imr=imresize(Img,[512 ,512]);
bw=rgb2gray(Imr);
fI= medfilt2(bw);
%fI=im2bw(fI);
%Counting the black pixels
Co2=0;
for i=1:512
    for j=1:512
        if(fI(i,j)==0)
            Co2=Co2+1;
        end
    end
end

    if(Co==Co2)
        set(handles.edit7,'string','Matched')
    else
        set(handles.edit7,'string','Unmatched')
    end;


